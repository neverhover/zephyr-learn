void OLED_ShowNum(u8_t x,u8_t y,u32_t num,u8_t len,u8_t size)
{           
    u8_t t,temp;
    u8_t enshow=0;                           
    for(t=0;t<len;t++)
    {
        temp=(num/mypow(10,len-t-1))%10;
        if(enshow==0&&t<(len-1))
        {
            if(temp==0)
            {
                OLED_ShowChar(x+(size/2)*t,y,' ',size,1);
                continue;
            }else enshow=1; 

        }
        OLED_ShowChar(x+(size/2)*t,y,temp+'0',size,1); 
    }
} 
/*************************************************************************/
/*函数功能: 显示字符串                                                                            */
/*入口参数：                                                                                                                          */
/*                      x,y:起点坐标                                                */
/*                      size:字体大小                                                          */
/*                      *p:字符串起始地址                                                               */ 
/*************************************************************************/
void OLED_ShowString(u8_t x,u8_t y,const u8_t *p,u8_t size)
{   
    while((*p<='~')&&(*p>=' '))//判断是不是非法字符!
    {       
        if(x>(128-(size/2))){x=0;y+=size;}
        if(y>(64-size)){y=x=0;OLED_GRAM_clean();}
        OLED_ShowChar(x,y,*p,size,1);    
        x+=size/2;
        p++;
    }  

}  






