#define OLED_WRITE_CMD(x) oled_wr_byte((x), OLED_DC_CMD)
#define OLED_WRITE_DATA(x) oled_wr_byte((x), OLED_DC_DATA)

u8_t OLED_GRAM[128][8];

//模拟SPI的方式写入数据
void oled_wr_byte(u8_t data, u8_t cmd){
    u8_t i;
    if(cmd == OLED_DC_CMD){
        OLED_SET_DC();
    }else{
        OLED_CLR_DC();
    }
    OLED_CLR_CS();
    //一个位一个位的从Master输出到Slave
    for(i=0; i<8; i++){
        OLED_CLR_CLK();
        if(data & 0x80)
            OLED_SET_MOSI();
        else
            OLED_CLR_MOSI();
        OLED_SET_CLK();
        data = data << 1;
    }
    OLED_SET_CS();
    OLED_SET_DC();
}

void OLED_GRAM_clean(){
    u8_t i,n;
    //128*64 bit = 128 * 8 byte
    for(i=0; i < 8; i++){
        for(n=0;n<128;n++)
            OLED_GRAM[n][i]=0;
    }
}

void OLED_GRAM_refresh(){
    u8_t i,n;
    //128*64 bit = 128 * 8 byte
    for(i=0; i < 8; i++){
        
		OLED_WRITE_CMD (0xb0+i);    //设置页地址(0~7)
		OLED_WRITE_CMD (0x00);      //设置显示位置—列低地址
        OLED_WRITE_CMD (0x10);      //设置显示位置—列高地址
        for(n=0;n<128;n++)
            OLED_WRITE_DATA(OLED_GRAM[n][i]);
    }
}

void OLED_Set_pos(u8_t x, u8_t y) {
	OLED_WRITE_CMD(0xb0+y);
	OLED_WRITE_CMD(((x&0xf0)>>4)|0x10);
	OLED_WRITE_CMD((x&0x0f)|0x01); 
}

void OLED_Display_On(void)
{
	OLED_WRITE_CMD(0X8D);  //SET DCDC
	OLED_WRITE_CMD(0X14);  //DCDC ON
	OLED_WRITE_CMD(0XAF);  //DISPLAY ON
}
   
void OLED_Display_Off(void)
{
	OLED_WRITE_CMD(0X8D);  //SET DCDC
	OLED_WRITE_CMD(0X10);  //DCDC OFF
	OLED_WRITE_CMD(0XAE);  //DISPLAY OFF
}




/*************************************************************************/
/*函数功能: 画点                                                          */
/*入口参数：                                                             */
/*                      x：横坐标       0~127                               */
/*                      y：纵坐标               0~63                     */
/*                      dot:0,清空;1,填充                                                                                */             
/*************************************************************************/
void OLED_DrawPoint(u8_t x,u8_t y,u8_t t)
{
    u8_t pos,bx,temp=0;
    if(x>127||y>63)return;//超出范围了.
    pos=7-y/8;
    bx=y%8;
    temp=1<<(7-bx);
    if(t)OLED_GRAM[x][pos]|=temp;
    else OLED_GRAM[x][pos]&=~temp;     
}
/*************************************************************************/
/*函数功能: 填充                                                          */
/*入口参数：                                                                                                                          */
/*                      x1,y1,x2,y2 填充区域的对角坐标                              */
/*                      确保x1<=x2;y1<=y2 0<=x1<=127 0<=y1<=63                                         */
/*                      dot:0,清空;1,填充                                                                                */             
/*************************************************************************/
void OLED_Fill(u8_t x1,u8_t y1,u8_t x2,u8_t y2,u8_t dot)  
{  
    u8_t x,y;  
    for(x=x1;x<=x2;x++)
    {
        for(y=y1;y<=y2;y++)OLED_DrawPoint(x,y,dot);
    }                                                       
    OLED_GRAM_refresh();//更新显示
}

/*************************************************************************/
/*函数功能: 在指定位置显示一个字符,包括部分字符                             */
/*入口参数：                                                                                                                          */
/*                      x:0~12                                                     */
/*                      y:0~63                                                                      */
/*                      mode:0,反白显示;1,正常显示                                                       */ 
/*            size:选择字体 16/12                                        */
/*************************************************************************/
void OLED_ShowChar(u8_t x,u8_t y,u8_t chr,u8_t size,u8_t mode)
{                   
    u8_t temp,t,t1;
    u8_t y0=y;
    u8_t csize=(size/8+((size%8)?1:0))*(size/2);      //得到字体一个字符对应点阵集所占的字节数
    chr=chr-' ';//得到偏移后的值        
    for(t=0;t<csize;t++)
    {   
        if(size==12)temp=asc2_1206[chr][t];         //调用1206字体
        else if(size==16)temp=asc2_1608[chr][t];    //调用1608字体
        //else if(size==24)temp=asc2_2412[chr][t];    //调用2412字体
        else return;                                //没有的字库
        for(t1=0;t1<8;t1++)
        {
            if(temp&0x80)OLED_DrawPoint(x,y,mode);
            else OLED_DrawPoint(x,y,!mode);
            temp<<=1;
            y++;
            if((y-y0)==size)
            {
                y=y0;
                x++;
                break;
            }
        }    
    }          
}
//m^n函数
u32_t mypow(u8_t m,u8_t n)
{
    u32_t result=1;    
    while(n--)result*=m;    
    return result;
}                 
